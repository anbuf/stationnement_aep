<li><b>J'habite encore chez mes parents et je n'ai pas une preuve de résidence à mon nom, que dois-je faire?</b><br>
Dans ce cas, certaines preuves de résidence moins officielles, tel qu'un compte de téléphone cellulaire ou un relevé de compte bancaire, pourront faire l'affaire, en autant qu'elles soient à votre nom et que l'adresse soit celle de votre demande.
</li>
<li><b>Quel est le prix du permis de stationnement 2013-2014?</b><br>
Le prix est de 294,60$ (taxes incluses) auquel il faut ajouter le dépôt de 30$. Pour l'hiver, le prix sera de 147,30$, plus le dépôt de 30$. Finalement, le prix pour l'horaire de soir sera de 274,54$, plus dépôt de 30$.
</li>
<li><b>Est-ce qu'une raison médicale est considérée lors des demandes?</b><br>
Oui, dans la mesure où ceci est dans votre intéret. Il faut considérer que les stationnements de classe C que nous offrons se situent à l'extérieur des murs de Polytechnique.
</li>

