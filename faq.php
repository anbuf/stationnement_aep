<li><b>Que dois-je faire pour obtenir un permis de stationnement pour l'année scolaire 2013-2014?</b><br>
Il faut que vous présentiez au moins (3) preuve de résidence à votre nom joints avec votre demande : <br>
1) Une copie (recto) de votre certificat d'assurance valide pour la période en cours avec inscription claire de l'adresse.<br>
2) Une copie (recto/verso) de votre permis de conduire. <br>
3) Au moins 1-2 factures (copies) de preuves de résidences (voir ci-dessous).<br>
Ces documents sont obligatoires pour obtenir un permis de stationnement.
</li>
